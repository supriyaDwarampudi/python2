# check if the number is even or odd

# if number is divisible by 2 and gives remainder as 0 then it consider to be as even number.

# if number is not divisible by 2 and gives remainder as 1 then it consider to be as odd number.

# CODE:

n=float(input('Enter a number'))
if n%2==0:
    print('Given number is even number')
else:
    print('Given number is odd number')

